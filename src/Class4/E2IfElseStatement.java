package Class4;

public class E2IfElseStatement {
    public static void main(String[] args){
        //String is non-primitive data type and we can't use
        // == sign
        String name = "Strawberry";

        if (name.equals("John")){
            System.out.println("I like the zoo");
        }else{
            System.out.println("I like programing");
        }
    }
}
