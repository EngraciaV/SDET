package Class5;

public class E5LogicalOperators {
    public static void main(String[] args) {

        /*
        NOT (!) - it can only be used with a boolean
        it flips the boolean values for example
        it will change it to false and if it is false it will be true
         */
        boolean flag = false;
        // ! operator will reverse the condition
        // and it will get printed

        System.out.println(!flag);
        System.out.println(flag);
    }
}
